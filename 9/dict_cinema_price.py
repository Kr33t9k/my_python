films = {'Пятница': {10: 250, 16: 350, 20: 450}, 'Чемпионы': {10: 250, 13: 350, 16: 350}, 'Пернатая': {10: 350, 14: 350, 18: 450}}
datas = {'Сегодня': 1, 'Завтра': 0.95}
a = films[input("Выберите фильм: ")][int(input("Выберите время: "))] * datas[input("Выберите дату: ")]
kol = int(input('Выберите количество: '))
if kol >= 20:
    kol = kol * 0.8
print(f'Цена билета составит: {a * kol} рублей.')